package chapter1;

public class Variables {
	  public static void main(String[] args) {
	
	    int intNum = 6;
	    intNum++;
	    intNum *= 3;
	    System.out.println(intNum);

	    double doubleNum;
	    doubleNum = 14.8;
	    doubleNum = 12 - 6.5;
	    doubleNum /= 2;
	    System.out.println(doubleNum);

	    boolean a = true;
	    a = false;
	    System.out.println(a);

	    String str = "Hello, world!";
	    System.out.println(str); 
	  }
	}